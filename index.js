function add(a, b) {
  return a + b
}

function subtract(a, b) {
  return a - b
}

function multiply(a, b) {
  return a * b
}

function divide(a, b) {
  return a / b
}

function inc(a) {
  return a + 1
}

function dec(a) {
  return a - 1
}

function makeInt(n) {
  var base = 10
  var parsed = parseInt(n, base)
  parseInt("n", base)
  return parsed 
}

function preserveDecimal(n) {
  var parsed = parseFloat(n)
  parseFloat(n)
  return parsed
}